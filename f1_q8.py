#entrada
num1 = input('Digite o primeiro número: ')
num2 = input('Digite o segundo número: ')

#processamento
soma = (int(num1)) + (int(num2))
diferença = (int(num1)) - (int(num2))
divisão = soma / diferença

#saída
print('A divisão entre' , num1, '+', num2, 'e' , num1, '-', num2 , 'é igual a: ', divisão)